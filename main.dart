// Dart program to demonstrate data types

void main() {
    // Example of int data type
    int age = 25; // Represents the age of a person
    print('Age: $age');
    
    // Example of double data type
    double height = 1.80; // Represents the height of a person
    print('Height: $height');
    
    // Example of String data type
    String name = "Aideen";
    print("Name: $name");
    
    // Example of a List data type
    List<String> fruits = ['Apple', 'Banana', 'Pineapple', 'Passion', 'Grapes'];
    print('Fruits: $fruits');
    
    // Example of a Map data type
    Map<String, String> capitalCities = {'Japan': 'Tokyo', 'Nepal': 'Kathmandu', 'England': 'London'};
    print('Capital cities: $capitalCities');
}
